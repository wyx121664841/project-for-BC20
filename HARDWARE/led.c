void LCD_ShowChar(uint16_t x,uint16_t y,uint8_t num,uint8_t size,uint8_t mode)
{
    uint8_t temp,t1,t;
    uint16_t y0=y;
    uint32_t colortemp=POINT_COLOR_24;
    //设置窗口
    num=num-' ';//得到偏移后的值
    if(!mode) //非叠加方式
    {
        for(t=0; t<size; t++)
        {

//			if(size==12)temp=asc2_1608[num][t];  //调用1206字体
//			else temp=asc2_1608[num][t];		 //调用1608字体
            for(t1=0; t1<8; t1++)
            {
                if(temp&0x80)POINT_COLOR_24=colortemp;
                else POINT_COLOR_24=BACK_COLOR_24;
                LCD_DrawPoint(x,y);
                temp<<=1;
                y++;
                if(x>=lcddev.width) {
                    POINT_COLOR_24=colortemp;    //超区域了
                    return;
                }
                if((y-y0)==size)
                {
                    y=y0;
                    x++;
                    if(x>=lcddev.width) {
                        POINT_COLOR_24=colortemp;    //超区域了
                        return;
                    }
                    break;
                }
            }
        }
    } else//叠加方式
    {
        for(t=0; t<size; t++)
        {
//			if(size==12)temp=asc2_1608[num][t];  //调用1206字体
//			else temp=asc2_1608[num][t];		 //调用1608字体
            for(t1=0; t1<8; t1++)
            {
                if(temp&0x80)LCD_DrawPoint(x,y);
                temp<<=1;
                y++;
                if(x>=lcddev.height) {
                    POINT_COLOR_24=colortemp;    //超区域了
                    return;
                }
                if((y-y0)==size)
                {
                    y=y0;
                    x++;
                    if(x>=lcddev.width) {
                        POINT_COLOR_24=colortemp;    //超区域了
                        return;
                    }
                    break;
                }
            }
        }
    }
    POINT_COLOR_24=colortemp;
}
