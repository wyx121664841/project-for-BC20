//画线
//x1,y1:起点坐标
//x2,y2:终点坐标
void LCD_DrawLine_c(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2,uint32_t color)
{
    uint16_t t;
    int xerr=0,yerr=0,delta_x,delta_y,distance;
    int incx,incy,uRow,uCol;
    delta_x=x2-x1; //计算坐标增量
    delta_y=y2-y1;
    uRow=x1;
    uCol=y1;
    if(delta_x>0)incx=1; //设置单步方向
    else if(delta_x==0)incx=0;//垂直线
    else {
        incx=-1;
        delta_x=-delta_x;
    }
    if(delta_y>0)incy=1;
    else if(delta_y==0)incy=0;//水平线
    else {
        incy=-1;
        delta_y=-delta_y;
    }
    if( delta_x>delta_y)distance=delta_x; //选取基本增量坐标轴
    else distance=delta_y;
    for(t=0; t<=distance+1; t++ ) //画线输出
    {
        LCD_DrawPoint_c(uRow,uCol,color);//画点
        xerr+=delta_x ;
        yerr+=delta_y ;
        if(xerr>distance)
        {
            xerr-=distance;
            uRow+=incx;
        }
        if(yerr>distance)
        {
            yerr-=distance;
            uCol+=incy;
        }
    }
}
////椭圆形指定1丈填充////////////////参数：起点x 坐标，起点 y坐标，宽，高，颜色///////
void LCD_Fill_oval(uint16_t x,uint16_t y,uint8_t width,uint8_t height,uint32_t color)
{
    int a,b,x0,y0;
    uint16_t i;
    a=width/2;//长短轴
    b=height/2;
    x0=x+a;

    //y0=sqrt(((a*a*b*b)-b*b*(x0-x)*(x0-x))/(a*a));
    for(i=x0; i>(x-a); i--)
    {

        y0=sqrt(((a*a*b*b)-b*b*(x0-x)*(x0-x))/(a*a));//求y坐标
        LCD_DrawLine_c(x0, y-y0, x0, y+y0,color);
        x0--;
    }

}


